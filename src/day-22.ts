type Reindeer = '💨' | '💃' | '🦌' | '🌟' | '☄️' | '❤️' | '🌩️' | '⚡' | '🔴';

type Grid = Reindeer[][][]

type Validate<TGrid extends Grid> =
  false extends (
    ValidateRows<TGrid> |
    ValidateColumns<TGrid> |
    ValidateRegios<TGrid>
  ) ? false : true

type ValidateUnion<TUnion> =
  Exclude<Reindeer, TUnion> extends never ? true : false

type ValidateRows<
  TGrid extends Grid,
  $Row = 0 | 1 | 2 | 3 | 4 | 5 | 6 | 7 | 8
> =
  $Row extends number
    ? ValidateUnion<TGrid[$Row][number][number]>
    : never

type ValidateColumns<
  TGrid extends Grid,
  $Subrow = 0 | 1 | 2,
  $Column = 0 | 1 | 2
> =
  $Subrow extends number
    ? $Column extends number
      ? ValidateUnion<TGrid[number][$Subrow][$Column]>
      : never
    : never

type ValidateRegios<
  TGrid extends Grid,
  $RowTuple = [0 | 1 | 2] | [3 | 4 | 5] | [6 | 7 | 8],
  $Subrow = 0 | 1 | 2
> =
  $RowTuple extends Array<infer $Row extends number>
    ? $Subrow extends number
      ? ValidateUnion<TGrid[$Row][$Subrow][number]>
      : never
    : never

